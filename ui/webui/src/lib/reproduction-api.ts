/** Map a backend event_type to the front-end phase it represents. */
export function eventToPhase(eventType: string): typeof FIVE_PHASES[number]['key'] | null {
  if (eventType === 'extract.done')   return 'extract';
  if (eventType === 'data.fetched')   return 'data';
  if (eventType === 'backtest.done')  return 'backtest';
  if (eventType === 'wiki.written')   return 'analyze';
  if (eventType === 'finalize.done')  return 'finalize';
  return null;
}