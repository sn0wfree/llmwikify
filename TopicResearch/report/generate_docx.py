def add_heading_with_style(doc, text, level=1, color=None):
    """添加带样式的标题"""
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    if level == 1:
        p.paragraph_format.space_before = Pt(18)
        run = p.add_run(text)
        run.font.size = Pt(18)
        run.font.bold = True
        if color:
            run.font.color.rgb = color
    elif level == 2:
        run = p.add_run(text)
        run.font.size = Pt(14)
        run.font.bold = True
    elif level == 3:
        run = p.add_run(text)
        run.font.size = Pt(12)
        run.font.bold = True
    return p

def add_paragraph(doc, text, indent=True, bold=False, size=None):
    """添加段落"""
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Pt(22) if indent else Pt(0)
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text)
    if bold:
        run.font.bold = True
    if size:
        run.font.size = size
    return p

def add_table(doc, data, header=True, col_widths=None):
    """添加表格"""
    rows = len(data)
    cols = len(data[0])
    table = doc.add_table(rows=rows, cols=cols)
    table.style = 'Light Grid Accent 1'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    for i, row_data in enumerate(data):
        row = table.rows[i]
        for j, cell_text in enumerate(row_data):
            cell = row.cells[j]
            cell.text = str(cell_text)
            for para in cell.paragraphs:
                para.paragraph_format.first_line_indent = Pt(0)
                para.paragraph_format.space_after = Pt(2)
                for run in para.runs:
                    if header and i == 0:
                        run.font.bold = True
                        run.font.size = Pt(10)
                    else:
                        run.font.size = Pt(9.5)
            cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER

    if col_widths:
        for i, w in enumerate(col_widths):
            for row in table.rows:
                row.cells[i].width = w
    return table

def add_callout(doc, text, color=RGBColor(180, 83, 9)):
    """添加洞察框"""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Pt(22)
    p.paragraph_format.right_indent = Pt(22)
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(8)
    p.paragraph_format.line_spacing = 1.5
    run = p.add_run(text)
    run.font.bold = True
    run.font.color.rgb = color
    return p


